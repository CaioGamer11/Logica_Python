Bim= int(input("Qual seu bimestre?"))
if Bim == 1:
    print("Você está no primeiro bimestre.")
elif Bim == 2:
    print("Você está no segundo bimestre.")
elif Bim == 3:
    print("Você está no terçeiro bimestre.")
elif Bim == 4:
    print("Você está no quarto bimestre.")
else:
    print("Isto não é um bimestre válido.")